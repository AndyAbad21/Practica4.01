    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            this.setActualizarTabla();
        }catch(RuntimeException ex)
        {
            JOptionPane.showMessageDialog(this,"El profesor con el codigo:("+
                    jTextField2.getText()+") no existe","Error eliminar Profesor",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int llave=JOptionPane.showConfirmDialog(this,
                "Esta seguro que desea eliminar la profesor","Eliminar la profesor",
                JOptionPane.OK_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE);
        if(llave==0)
        {
            try
            {
                asignaturaControl.eliminarProfesor(jTextField1.getText(),jTextField2.getText());
                this.setActualizarTablaDefecto();
                jTextField1.setText("");
                jTextField2.setText("");
                JOptionPane.showMessageDialog(null,"El Profesor se elimino con exito",
                        "Mensaje de informacion",JOptionPane.INFORMATION_MESSAGE);
            }catch(NullPointerException ex)
            {
                JOptionPane.showMessageDialog(this,
                        "El profesor con el codigo:("+jTextField1.getText()+") no existe",
                        "Eliminar Profesor",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

