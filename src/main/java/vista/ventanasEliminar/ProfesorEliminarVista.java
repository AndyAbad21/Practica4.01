    public void setActualizarTabla()
    {
        String[] encabezado=new String[6];
        encabezado[0]=resourceBundle.getString("JTABLETPV1");
        encabezado[1]=resourceBundle.getString("JTABLETPV2");
        encabezado[2]=resourceBundle.getString("JTABLETPV3");
        encabezado[3]=resourceBundle.getString("JTABLETPV4");
        encabezado[4]=resourceBundle.getString("JTABLETPV5");
        encabezado[5]=resourceBundle.getString("JTABLETPV6");
        
        var datos=new Object[1][6];
        Asignatura asignatura=asignaturaControl.listar().get(asignaturaControl.buscarAsignaturaPorCodigo(jTextField1.getText()));
        Profesor profesor=asignatura.getProfesorList().get(asignaturaControl.buscarProfesorPorCodigo(jTextField1.getText(),jTextField2.getText()));
        datos[0][0]=profesor.getCodigo();
        datos[0][1]=profesor.getNombre();
        datos[0][2]=profesor.getCorreo();
        datos[0][3]=profesor.getTitulo();
        datos[0][4]=profesor.getDiaNacimiento()+"/"+profesor.getMesNacimiento()+"/"+profesor.getAnioNacimiento();
        datos[0][5]=profesor.getAniosTrabajo();
        
        TableModel modeloTabla=new DefaultTableModel(datos,encabezado);
        jTable1.setModel(modeloTabla);
        
    }
